#include "dwc_ddrctl_sequences_utils.h"

/** @brief method to get max value from list
 */
int8_t max_from_list(int8_t data[], uint32_t size)
{
    uint32_t i;
    int8_t max_value = SCHAR_MIN;

    for(i = 0; i < size; i++){
        max_value = max(max_value, data[i]);
    }
    return max_value;
}
